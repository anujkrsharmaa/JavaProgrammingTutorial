package basicsOfJava;

public class variablesint {
    public static void main(String[] args) {
        int x;
        x = 500;
        System.out.println(x);
        x = 200;
        System.out.println(x);
        x = x + 109;
        System.out.println(x);
        x = x - 10;
        System.out.println(x);
        x = x * 10;
        System.out.println(x);
        x = x / 100;
        System.out.println(x);
    }
}
